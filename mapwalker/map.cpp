class Map{
	/*
	 *  3  Terrains
	 *****
	 *  0: Water
	 *  1: Plains
	 *  2: Woods
	 *****
	 * Water : Unpassable
	 * Plains: Passable, takes 1 Time.
	 * Woods : Passable, takes 2 Time.
	 */
protected:
	int tiles[30][80];
	int x;
	int y;
	
public:
	Map(int, int);
  int set(int[][80]);
	int show();
	int set_pos(int, int);
};

Map::Map(int xcurr, int ycurr){
	
	for (int i = 0; i < 80; i++){
		for (int j = 0; j < 30; j++){
			tiles[j][i] = 0;
		}
	}

	x = xcurr;
	y = ycurr;
	
}

Map::set(int intiles[][80]){
	for (int j = 0; j < 30; j++){
		
		for (int i = 0; i < 80; i++){
			
			tiles[j][i] = intiles[j][i];
		}
	}
}

Map::set_pos(int xoff, int yoff){

	if (!((x + xoff) < 0 ||
				(x + xoff) >= 80 ||
				(y + yoff) < 0 ||
				(y + yoff) >= 30 ||
				(tiles[y+yoff][x+xoff] == 0)))
		{
			x += xoff;
			y += yoff;
		}
}

Map::show(){
	std::cout << "Map:";

	std::cout << std::endl;
	for (int j = 0; j < 30; j++){
		
		for (int i = 0; i < 80; i++){

			if (i == x && j == y){
				std::cout << 'X';
			}
			else {
				switch (tiles[j][i]){
				case 0:
					std::cout << '~';
					break;

				case 1:
					std::cout << '_';
					break;
				
				case 2:
					std::cout << '^';
					break;

				default:
					throw "[Map] Fatal Error showing Map.";
				}
			}
		}
		std::cout << std::endl;
		
	}
}

